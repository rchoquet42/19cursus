#include "libft.h"

int		ft_strcmp_no_case(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (ft_tolower(s1[i]) == ft_tolower(s2[i]))
	{
		if (s1[i] == '\0')
			return (0);
		i++;
	}
	return (s1[i] - s2[i]);
}
