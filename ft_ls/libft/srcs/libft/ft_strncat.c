#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	unsigned long i;
	unsigned long j;

	i = 0;
	j = 0;
	while (s1[i])
		i++;
	while (j < n && s2[j] != '\0')
	{
		s1[i] = s2[j];
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
