int	ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb > 2147395600)
		return (0);
	if (nb == 1)
		return (1);
	if (nb <= 0)
		return (0);
	while ((i * i) <= nb)
	{
		if ((i * i) == nb)
			return (i);
		i++;
	}
	return (0);
}
